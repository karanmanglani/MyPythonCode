### Adapted from book How to solve it - by George Polya
## Ask the following questiions ---

#1 Can i restate the problem in my own words?
#2 What are the inputs that go into the problem? // Really Important
#3 What are the expected outputs?
#4 Can output be determined from input? (Do we have enough Input?)
#5 How should I label important peices of data that are a part of the problem?

### Example
### Function to accept two number and return their sum

